public class calculadora {
    public static float sumar(float uno, float dos){
        float  sumando = uno + dos;
        return sumando;
    }
     public static float restar(float uno, float dos){
        float  restando = uno - dos;
        return restando;
    }
     
     public static float multiplicar(float uno, float dos){
         float  multiplicando = uno * dos;
        return multiplicando;
    }
      public static float dividir(float uno, float dos){
        float  dividiendo = uno + dos;
        return dividiendo;
    }
     
     
    
}
